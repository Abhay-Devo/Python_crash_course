""" This will demonstrate how we can type cast any type of datatype into other datatype..."""

a = 55
b = "9579234"
c = 42.94


#Printing what is the datatype of that particular variable.

print(type(a))  
print(type(b))
print(type(c))


#Type casting one datatype into another and printing them.

print("This is now an string value:",str(a))    #Typecasting integer into string.
print("This is now an integer value:",int(b))    #Typecasting string into integer.
print("This is now an string value:",str(c))    #Typecasting floating point into string.
print("This is now an integer value:",int(c))    #Typecasting floating point into integer.


