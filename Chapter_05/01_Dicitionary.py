# Dictonary is a set of key-value pair
# It is mutable and can be accesed through index(but in diff way)

marks = {
    "rohan" : 100,
    "arjun" : 50,
    "sachin" : 35
}

# Accessing a value in dictionary
print(marks)


# Accessing a value in dictionary through indexing
# Here instead of [0],or[1] indexing, we give the key as index and then it return the value associated to it.
print(marks["rohan"])     

