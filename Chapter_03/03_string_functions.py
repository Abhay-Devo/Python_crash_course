# Here we will see some functions of strings...

string1 = "harry is a good boy"

print("Printing the length of the string:",len(string1))
print("\n")

print("Printing the No. of 'o' char in the string:",string1.count("o"))
print("\n")

print("Printing the capatilize form of the string:",string1.capitalize())
print("\n")

print("Printing the string in uppercase:",string1.upper())
print("\n")

print("Printing where is the 'is' workd in the string:",string1.find("is"))
print("\n")

print("Printing the string after replacing word:",string1.replace("is", "are"))
print("\n")

