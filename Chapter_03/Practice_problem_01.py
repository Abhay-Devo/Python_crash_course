# Add two strings together using (f-string)

name = input("Enter your name:")

#This is called f-String by using this you can add two strings together easily.
print(f"Good Morning, {name}, here it's very simple") 

#If you don't use f-string you have to do= 
print("Good Morning, "+name) 

# its a very long process, if want to add multiple strings.
print("Good Morning, "+name+",Its a very long process") #.....etc